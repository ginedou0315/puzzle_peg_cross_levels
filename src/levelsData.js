// Function to count initial pegs for a given board setup
export const countInitialPegs = (boardSetup) => {
  let pegCount = 0;
  boardSetup.forEach((row) => {
    row.forEach((cell) => {
      if (cell === 2) {
        // 2 represents a peg
        pegCount++;
      }
    });
  });
  return pegCount;
};
